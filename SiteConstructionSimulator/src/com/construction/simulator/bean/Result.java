package com.construction.simulator.bean;

public class Result {

	private int communicationOverhead;
	private int fuelUsage;
	private int unclearedSquares;
	private int protectedTreeDamage;
	private int paintDamage;
	private int total;
	
	
	public int getCommunicationOverhead() {
		return communicationOverhead;
	}
	public void setCommunicationOverhead(int communicationOverhead) {
		this.communicationOverhead = communicationOverhead;
	}
	public int getFuelUsage() {
		return fuelUsage;
	}
	public void setFuelUsage(int fuelUsage) {
		this.fuelUsage = fuelUsage;
	}
	public int getUnclearedSquares() {
		return unclearedSquares;
	}
	public void setUnclearedSquares(int unclearedSquares) {
		this.unclearedSquares = unclearedSquares;
	}
	public int getProtectedTreeDamage() {
		return protectedTreeDamage;
	}
	public void setProtectedTreeDamage(int protectedTreeDamage) {
		this.protectedTreeDamage = protectedTreeDamage;
	}
	public int getPaintDamage() {
		return paintDamage;
	}
	public void setPaintDamage(int paintDamage) {
		this.paintDamage = paintDamage;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
	
	
}
